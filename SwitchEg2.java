package ControlStatement;

public class SwitchEg2 {

	public static void main(String[] args) {
		char a='H';
		switch(a){
		case 'A':
			System.out.println("A");
			break;
		
		
		case 'H':
			System.out.println("Harinder");
			break;
		
		
		case 'd':
			System.out.println("d");
			break;
		
		
		default:System.out.println("Not Match");
		
		}

	}

}
